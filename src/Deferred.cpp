void genNoise(bonobo::Texture *nt) {
	LogInfo("Generating new noise!\n");
	srand(time(NULL));
	PerlinNoise pn(rand());
	std::vector<float> nnoise;
	float max = static_cast<float>(pn.noise(0, 0, 0))
		, min = static_cast<float>(pn.noise(0, 0, 0));
	for (size_t i = 0; i < pns; i++)
	{
		for (size_t j = 0; j < pns; j++)
		{
			for (size_t k = 0; k < pns; k++)
			{
				//noise[i][j][k] =(float) pn.noise((double)i/255, (double)j / 255, (double)k / 255);
				float val = static_cast<float>(pn.noise((double)i / pns, (double)j / pns, (double)k / pns));
				nnoise.push_back(val);
				if (val < min)
					min = val;
				if (val > max)
					max = val;
			}
		}
	}
	std::cout << "min: " << min << std::endl << "max: " << max << std::endl;
	float med = (max - min) / 2;
	float slope = (1 - (-1)) / (max - min);

	for (size_t i = 0; i < pns*pns*pns; i++)
	{
		nnoise[i] = -1 + slope * (nnoise[i] - min);
	}
	glGenTextures(1, &nt->mId);
	glBindTexture(GL_TEXTURE_3D, nt->mId);
	glTexImage3D(GL_TEXTURE_3D, 0, GL_R32F, pns, pns, pns, 0, GL_RED, GL_FLOAT, nnoise.data());
	bonobo::checkForErrors();
	glTexParameteri(GL_TEXTURE_3D, GL_TEXTURE_MIN_FILTER, GL_LINEAR);
	glTexParameteri(GL_TEXTURE_3D, GL_TEXTURE_MAG_FILTER, GL_LINEAR);
	glTexParameteri(GL_TEXTURE_3D, GL_TEXTURE_WRAP_S, GL_MIRRORED_REPEAT);
	glTexParameteri(GL_TEXTURE_3D, GL_TEXTURE_WRAP_T, GL_MIRRORED_REPEAT);
	glTexParameteri(GL_TEXTURE_3D, GL_TEXTURE_WRAP_R, GL_MIRRORED_REPEAT);
	nt->mTarget = bonobo::TEXTURE_3D;
	glBindTexture(GL_TEXTURE_3D, 0);
	bonobo::checkForErrors();
}


