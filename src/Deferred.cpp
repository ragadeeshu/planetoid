static GLuint loadCube(GLuint& vboId, GLsizei& verticesNb, bonobo::ShaderProgram* shader)
{


	verticesNb = verPerSide *verPerSide *verPerSide;
	std::vector<float> points;
	//float vertexArrayData[verPerSide *verPerSide *verPerSide * 3] = { 0 };
	for (size_t i = 0; i < verPerSide; i++)
	{
		for (size_t j = 0; j < verPerSide; j++)
		{
			for (size_t k = 0; k < verPerSide; k++)
			{
				points.push_back(i*stepSize - steps / 2);
				points.push_back(j*stepSize - steps / 2);
				points.push_back(k*stepSize - steps / 2);
				//vertexArrayData[i*verPerSide*verPerSide * 3 + j*verPerSide * 3 + k * 3] = i*stepSize - steps / 2;
				//vertexArrayData[i*verPerSide*verPerSide * 3 + j*verPerSide * 3 + k * 3+1] = j*stepSize - steps / 2;
				//vertexArrayData[i*verPerSide*verPerSide * 3 +j*verPerSide * 3 +k * 3+2] = k*stepSize - steps / 2;
				//std::cout << i*verPerSide*verPerSide * 3 + j*verPerSide * 3 + k * 3 << std::endl;

			}
		}
	}

	GLuint vaoId = 0u;
	GLint loc = glGetAttribLocation(shader->mId, "Vertex");
	bonobo::checkForErrors();
	glGenVertexArrays(1, &vaoId);
	bonobo::checkForErrors();
	glBindVertexArray(vaoId);
	bonobo::checkForErrors();
	{
		glGenBuffers(1, &vboId);
		bonobo::checkForErrors();
		glBindBuffer(GL_ARRAY_BUFFER, vboId);
		bonobo::checkForErrors();
		glBufferData(GL_ARRAY_BUFFER, verticesNb * 3 * sizeof(float), points.data(), GL_STATIC_DRAW);
		bonobo::checkForErrors();

		glVertexAttribPointer(loc, 3, GL_FLOAT, GL_FALSE, 0, 0);
		bonobo::checkForErrors();
		glEnableVertexAttribArray(loc);
		bonobo::checkForErrors();
	}
	glBindVertexArray(0u);
	bonobo::checkForErrors();
	glBindBuffer(GL_ARRAY_BUFFER, 0u);
	bonobo::checkForErrors();

	return vaoId;
}
